using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ConsoleApp1
{
    internal class for_loop
    {
        static void Main(string[] args)
        {

            for(int i=1; i<=10; i++)
            {
                Console.WriteLine(i);
            }


            for(int i=10; i>=1; i--)
            {
                Console.WriteLine(i);
            }
        }
    }
}
